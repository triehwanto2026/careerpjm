type CfitResultLike = {
  score?: number | null;
  total_questions?: number | null;
  categories?: Record<string, unknown> | null;
  interpretation?: string | null;
};

const toNumber = (value: unknown) => {
  const parsed = Number(value);
  return Number.isFinite(parsed) ? parsed : 0;
};

const clampRawScore = (value: number, max: number) => Math.max(0, Math.min(Math.max(1, max), Math.round(value)));

export const isCfitName = (name?: string | null) => {
  const upper = String(name || "").toUpperCase();
  return upper.includes("CFIT") || upper.includes("CULTURE FAIR");
};

export const getCfitIqInfo = (rawScore: number) => {
  const raw = Math.max(0, Math.min(49, Math.round(rawScore)));
  return CFIT_IQ_TABLE[raw] || CFIT_IQ_TABLE[0];
};

export const getCfitRawScore = (result: CfitResultLike) => {
  const categories = result.categories || {};
  const explicit = toNumber(
    categories["CFIT Raw Score"] ?? categories.cfit_raw_score ?? categories.raw_score ?? categories.correct_answers,
  );
  const max = Math.max(1, toNumber(result.total_questions) || toNumber(categories["CFIT Max Score"]) || 50);
  if (explicit > 0) return clampRawScore(explicit, max);

  const interpretedCorrect = String(result.interpretation || "").match(/(\d+)\s+jawaban\s+benar/i);
  if (interpretedCorrect) return clampRawScore(Number(interpretedCorrect[1]), max);

  const subtestKeys = ["S1 - Series", "S2 - Classifications", "S3 - Matrices", "S4 - Conditions", "Series", "Classifications", "Matrices", "Conditions"];
  const subtestSum = subtestKeys.reduce((sum, key) => sum + toNumber(categories[key]), 0);
  const classificationScore = toNumber(categories["S2 - Classifications"] ?? categories.Classifications);
  const looksLikeOptionScore = classificationScore > 14 || subtestSum > max;
  if (subtestSum > 0 && !looksLikeOptionScore) return clampRawScore(subtestSum, max);

  const fromScore = Math.round((toNumber(result.score) / 100) * max);
  return clampRawScore(fromScore, max);
};

export const getCfitIqInfoFromResult = (result: CfitResultLike) => {
  const max = Math.max(1, toNumber(result.total_questions) || toNumber(result.categories?.["CFIT Max Score"]) || 50);
  const raw = getCfitRawScore(result);
  return { ...getCfitIqInfo(raw), raw, max };
};

export const getCfitProfileRows = (result: CfitResultLike) => {
  const info = getCfitIqInfoFromResult(result);
  const categories = result.categories || {};
  const segments = [
    { key: "Series", alt: "S1 - Series", label: "Series", max: 13, note: "penalaran urutan dan kelanjutan pola" },
    { key: "Classifications", alt: "S2 - Classifications", label: "Classifications", max: 14, note: "kemampuan mengelompokkan dan membedakan karakteristik visual" },
    { key: "Matrices", alt: "S3 - Matrices", label: "Matrices", max: 13, note: "analisis relasi visual dan pola matriks" },
    { key: "Conditions", alt: "S4 - Conditions", label: "Conditions", max: 10, note: "penalaran kondisi, aturan, dan konsistensi visual" },
  ].map((segment) => {
    const raw = toNumber(categories[segment.alt] ?? categories[segment.key]);
    const pct = Math.round((raw / segment.max) * 100);
    const level = pct >= 80 ? "Sangat Tinggi" : pct >= 65 ? "Tinggi" : pct >= 45 ? "Sedang" : pct >= 30 ? "Rendah" : "Sangat Rendah";
    return { ...segment, raw, pct, level };
  });
  return segments.map((segment) => ({
    label: segment.label,
    value: `${segment.raw}/${segment.max}`,
    note: `${segment.level} - ${segment.note}`,
  }));
};

